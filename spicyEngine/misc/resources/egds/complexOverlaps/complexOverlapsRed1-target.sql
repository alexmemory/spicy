CREATE TABLE City(
id varchar(255) ,
name varchar(255) ,
region varchar(255)
);

CREATE TABLE Person(
id varchar(255) ,
name varchar(255) ,
age varchar(255),
carId varchar(255),
cityId varchar(255)
);

CREATE TABLE Car(
id varchar(255) ,
model varchar(255) ,
plate varchar(255) ,
makeId varchar(255)
);
